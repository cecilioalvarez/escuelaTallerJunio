var suma=function sumar(a,b) {
  return a+b;
};

var resta= function restar(a,b){
  return a-b;
};

var multiplica= function multiplica(a,b){
  return a*b;
};

var dividir= function dividir(a,b){
  return a/b;
};

exports.suma=suma;
exports.resta=resta;
exports.multiplica=multiplica;
exports.dividir=dividir;
